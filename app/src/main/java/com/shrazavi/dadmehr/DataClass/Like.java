package com.shrazavi.dadmehr.DataClass;

public class Like {
    String id;
    String user;
    String vakile;
    Boolean like;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getVakile() {
        return vakile;
    }

    public void setVakile(String vakile) {
        this.vakile = vakile;
    }

    public Boolean getLike() {
        return like;
    }

    public void setLike(Boolean like) {
        this.like = like;
    }
}
