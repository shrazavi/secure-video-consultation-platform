package com.shrazavi.dadmehr.DataClass;

public class Room {
    String id;
    String name;
    String userA;
    String userB;
    int readA;
    int readB;
    String date;
    String key;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserA() {
        return userA;
    }

    public void setUserA(String userA) {
        this.userA = userA;
    }

    public String getUserB() {
        return userB;
    }

    public void setUserB(String userB) {
        this.userB = userB;
    }

    public int getReadA() {
        return readA;
    }

    public void setReadA(int readA) {
        this.readA = readA;
    }

    public int getReadB() {
        return readB;
    }

    public void setReadB(int readB) {
        this.readB = readB;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
